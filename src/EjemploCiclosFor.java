public class EjemploCiclosFor {
    public static void main(String[] args) {
        for (int contador = 0; contador <= 10; contador++) { // for (inicializacion; condicion; incremento)
            System.out.println("contador = " + contador); //Cuadro de iteraciones
        } //cuando no necesitemos usar la variable incrementada, podemos usar el ciclo for
         //cuando necesitemos usar la variable incrementada, usamos el ciclo while
    }
}
